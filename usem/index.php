     <h2 align="center"><br>UNIVERSITY SCHOOL OF ENVIRONMENT MANAGEMENT</h2>
		 <p align="center" style="padding-left:10%;padding-right:10%;">
             <p align="justify">The University School of Environment Management was established in the year 1998 essentially to address future environmental problem and sustainable use of natural resources through quality education, training and research. The vision followed by the School is to actively pursue the goals of imparting quality education, training and facilitate research in the field of environment and natural resources management and also act as a think tank in policy matters related to environment management and sustainable development. The School is set up for the accomplishment of its nation to build a pool of trained and dedicated professionals who can contribute to the systematic development of policies, measures and programmes for environmental management and sustainable development at national and international levels. USEM has got Placement cell which actively facilitate student placement and the summer training. Two Student's forum Biophilia and SEED are active for the various students' activities. </p>
<p><strong>&nbsp;OBJECTIVES OF THE SCHOOL </strong></p>
<ul>
  <li>To provide quality education and training in environment management. </li>
  <li> To pursue and facilitate contemporary research in various facets of bioresources and environment using modern analytical and other tools such as geospatial techniques. </li>
  <li> To establish working linkages with industry and undertake research on environment related issues. </li>
  <li> To foster environmental awareness and promote the principles and practices of sustainable development. </li>
</ul>
<p><strong>PROGRAMMES OFFERED </strong></p>
<p>The School of Environment Management offers 02 years Masters Programmes in (i) <strong>Environment Management, </strong>(ii) <strong>Biodiversity and Conservation </strong>&amp; (iii) <strong>Natural Resource Management </strong>. These <strong></strong>programmes are aimed to generate professionals equipped with the fundamental scientific knowledge so as <strong></strong>to become responsive to the current and emerging environmental issues. The students acquire in depth and <strong></strong>comprehensive understanding of a wide variety of environmental analysis tools, methods and systems so that <strong></strong>they can play a leading role in the development and application of environmental policies and conservation <strong></strong>programmes for environmental management and sustainable development. Present intake for the courses is: 25 students for M. Sc. Environment Management (EM) from science/engineering background, 20 for M. Sc. Biodiversity and Conservation (B&amp;C) from any discipline of Biological/ Life Sciences and 25 students for M. Sc. Natural Resource Management (NRM) from science background. <strong></strong></p>
<p><strong>&nbsp;RESEARCH ACTIVITIES </strong></p>
<p align="justify">During the last couple of years the School has made concerted efforts in promoting research activities in the contemporary areas of research. The Ph.D Scholars of the School (42 nos.) working on different research topic share their results through monthly in-house seminars, national and international conference and by journals publications. On an average annually about 25-40 research papers are published from the School, some are having high impact factors. Presently, USEM is actively involved in carrying out research in the following areas: </p>
<ul>
  <li> Conservation Biology </li>
  <li> Glaciology </li>
  <li> Urban landscape- green space management </li>
  <li> Environmental Biotechnology </li>
  <li> Water and wastewater treatment </li>
  <li> Energy and Environment Interactions, Climate Change </li>
  <li> Environmental modelling </li>
  <li> Air, Water and land pollution, EIA </li>
  <li> Biosystematics of plants and animals </li>
  <li> Disaster management </li>
</ul>
<p><strong>INFRASTRUCTURAL FACILITIES </strong></p>
<p>USEM has state of the art laboratories for carrying out teaching and research. USEM boasts of having an advanced instrumentation system lab, water quality analysis laboratory, remote sensing / GIS laboratory, environment microbial technology laboratory, Energy and Climate Change, air pollution monitoring laboratory, biosystematics and conservation biology lab. </p>

