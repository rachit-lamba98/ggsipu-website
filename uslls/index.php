     <h2 align="center"><br>UNIVERSITY SCHOOL OF LAW AND LEGAL STUDIES</h2>
		 <p align="center" ><br><b>Vision:</b> To become an internationally recognized centre for education and research.</p>
		 <p align="justify" ><strong>Vision</strong> : University School of Law and Legal Studies envisages advancement of institutional expertise in the area of legal research and education by augmenting academic and professional excellence, developing critical and relentless engagement with legal theory and practice, synergizing law, legal research, legal education and action to further the quest for justice </p>
  <p align="justify" >Mission : University School of Law and Legal Studies aspires to open new vistas in the arena of law, legal studies, theoretical and clinical legal education through an incessant endeavour towards developing academic potential, critical analytical ability, advocacy, counseling and mediation skills so as to fully equip the students with learning which is intellectually stimulating, socially vital and professionally enriching. </p>
  <p align="justify" ><strong>Quality Policy</strong>: To realize its mission, the school finds adherence to the following standards absolutely essential: </p>
  <ul >
    <li>Adoption of multidisciplinary approach towards law and legal studies so as to develop greater synergy between classroom lectures, conventional professional and alternative practices in law. </li>
    <li>Encourage continuous engagement with and adoption of innovative approaches towards law, legal education, training and legal processes through capacity building of the faculty and facilitating access to advanced academic courses. </li>
    <li>Constant involvement with processes and practice of law in the professional, social and economic arenas of life by encouraging interaction between the institution and those involved in these processes. </li>
    <li>Development of technical, advocacy, communication and research skills not only through taught courses but also through practical training and engagement with the industry, community and the state. </li>
    <li>Expansion of facilities for advanced research in law, introduction of new and vibrant courses and for augmenting the capacity to undertake consultancies and projects with state, national and international agencies, industry etc. </li>
  </ul>
  <p align="justify" ><strong>Distinctiveness</strong> : Established in 2001, USLLS's objective was to run Five Year Integrated Professional Law Course and to serve as an institution of advanced legal studies. It was the first University Law School established in the vicinity of the Supreme Court, Delhi High Court, various subordinate courts, commissions, tribunals and various monitoring offices of national and international voluntary organizations. Its Faculty is selected prescribing higher qualification than the University Grants Commission (UGC). Admissions are made on the basis of Common Entrance Test; the intake every year is 60. It also offers LL.M, Ph.D. and Post Doctoral research facilities in law. </p>
  <p align="justify" >The approach of the law school is to develop a positive attitude in students and make them realize that the lawyers are an integral part of the system of adjudication, determination of policies and setting up of standards for regulation of various activities through relevant institutions. It, therefore, trains them for placements in judiciary , quasi-judicial bodies , regulatory authorities , commissions , voluntary organizations and other sectors . Keeping in view the status of litigation in court system, courses are also offered in Alternative Dispute Resolution (ADRs) and Client Counselling and Interviewing. These courses train a lawyer to offer alternative ways of dispute resolution outside the court system. </p>
  <p align="justify" >Interdisciplinary approach: The University School of Law and Legal Studies is enjoying the privilege of genuine academic leadership and interdisciplinary approach getting colour and functional orientation from the activities of the other schools of study at the University Campus: (1) School of Information Technology, (2) School of Biotechnology, (3) School of Environment Management, (4) School of Management Studies, (5) School of Chemical Technology, (6) School of Basic and Applied Science and School of Humanities and Social Sciences. </p>
