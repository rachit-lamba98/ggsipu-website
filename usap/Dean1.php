           <h3>Teaching/Research Experience:</h3>
           <p style="text-align:justify;"><br>
Dr. Chandra Shekhar Rai is a Professor with the University School of Information & Communication Technology since September, 2011. He served the University as Associate Professor from 2007 to 2011 and Reader from 2004 to 2007 . He was lecturer with the same School from 1999 to 2004. He obtained his M.E. degree in Computer Engineering from SGS Institute of Technology & Science, Indore in 1994 and completed Ph.D. in area of Neural Network from Guru Gobind Singh Indraprastha University in 2003. He has earlier worked as a lecturer at Guru Jambeshwar University , Hissar. He has many publications in International/national journals and conferences. He was conferred with Best Teacher Award of the University for the academic year 2007-2008 and second best researcher award of the University in the area of engineering and technology in 2010. He was Warden Boys Hostel from 2003-2013, Chairman, University Center for IT Services & Infrastructure Management (UCITIM) from 2007 to 2012, and Director, Research & Consultancy from 2012-2014. His teaching and research interests include: Artificial Neural Systems, Computer Networks and, Signal Processing.
