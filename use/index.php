     <h2 align="center"><br>UNIVERSITY SCHOOL OF EDUCATION</h2>
		 <p align="center" style="">The University School of Education aims to facilitate studies in all emerging areas of Education such as Educational Management, Planning and Finance, Teacher Education, Educational Technology, Inclusive Education, Distance Education etc. to prepare teacher educators who are well versed with the skills and competencies of effective and efficient teaching skills and research. The school shall foster a climate of life long learning and empower individuals to be torch bearers of social change by transforming the very face of Teacher Education. The school has emerged as a role model with respect to the pedagogical interventions ,innovations in research and creation of enriched teaching –learning environments which ignite minds through challenge and feedback. The school fosters the growth of the individual and the collective through constructing knowledge collaboratively and creating an open environment which nurtures the spirit of progressive Teacher Education. </p>
<p><strong>Vision </strong></p>
<p>The school aims to empower Teacher-Educators with knowledge and skills of the 21 st century, nurture passion for research and teaching and imbibe the spirit of social inquiry to ensue harmony and prosperity </p>
<p><strong>Mission </strong></p>
<p align="justify">University School of Education shall function as a cradle to prepare motivated &amp; dedicated educators, policy planners, administrators and most importantly life long learners who shall function as catalysts to create and sustain learning communicates </p>
<p>Which promote equality and equity in education. </p>
<p><strong>Programmes offered: </strong></p>
<ul>
  <li>Ph.D. </li>
  <li>M.Ed. (Regular) </li>
  <li>B.Ed (offered through affiliated Institutions) </li>
  <li>Advanced Diploma in Child Guidance and Counseling (offered by NIPCCD,an affiliated govt. organization) </li>
</ul>
<strong>Master of Education </strong>
<p align="justify">The Post Graduate Degree Programme in Education aims to prepare socially responsible and responsive teacher educators who are ready to face all challenges accompanying globalization. The school supports this endeavor through the provision of state of art infrastructure comprising spacious Lecture rooms, Seminar room, Conference hall, Educational Technology Laboratory, Psychology Laboratory,Language Laboratory,Resource Room, Book Bank and a rich library which houses a large number of books, Periodicals, Journals covering diverse themes of education. </p>
<p><strong>Programme Objectives: </strong></p>
<p>The M.Ed<strong>.</strong> programme has been designed keeping in view of the following objectives: </p>
<ul>
  <li>To produce dedicated Teacher Educators who believe in life long learning. </li>
  <li>Set high standards of professional competency of intellectual conviction and integrity. </li>
  <li>Have a clear futuristic vision and aspiration for improving the teaching learning process as professionals </li>
  <li>Be inspiring and motivational leaders in the school, community, nation and the world. </li>
  <li>To educate a new cadre of instructional leaders – teacher educators, school entrepreneurship and administrators who will have the capacity, skill and knowledge to create and sustain environments in modern day education. </li>
  <li>To equip the teachers with the knowledge &amp; skills of new technologies focusing on understanding the principles of organizational management, Leadership, systemic change. </li>
</ul>
<strong>Ph.D &nbsp; </strong>
<p>Ph.D Programme at the school aims at developing competence in the methodology of conducting research in various fields of Teacher Education so that it could go a long way in producing reliable and valid thesis with the objectives of : </p>
<ul>
  <li>&#149;&nbsp; To develop sound understanding of the methods of research in various fields of teacher education so that it could go a long way in producing reliable and valid thesis. </li>
  <li>&#149;&nbsp; To develop sound understanding of the methods of research. </li>
  <li>&#149;&nbsp; To understand and analyse research literature to identify gaps. </li>
  <li>&#149;&nbsp; To develop the ability to construct and use appropriate educational research tools. </li>
  <li>&#149;&nbsp; To apply appropriate statistical techniques for data analysis depending on the research designs. </li>
</ul>
<p>USE is devoted to the publication of research journals with papers of advance knowledge through research &amp; case functional areas of various streams of education at different levels. </p>
