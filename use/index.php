<ul>
  <li>Orientation Programme for M.Ed scholars of USE was organized in the month of August 2010. </li>
  <li> Special Guest Lectures were organized by the School and Prof. M.C. Sharma, Prof. Beena Shah and Dr. L.P. Gupta were invited as speakers time to time. </li>
