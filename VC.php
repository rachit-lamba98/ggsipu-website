<p>

A leading scientist in the area of biotechnology, Professor Anil K. Tyagi has actively pursued his research with emphasis towards the control and amelioration of tuberculosis, the disease that claims more than 30,000 human lives every week. For the last more than 15 years, Professor Tyagi's group has focused on two broad areas, (i) development of new vaccines against tuberculosis and (ii) Drug discovery programme for new potential drugs against tuberculosis. The Tuberculosis Vaccine Clinical Trial Expert Group of the Department of Biotechnology, Government of India, has recommended three new vaccine regimens developed by Dr. Tyagi's group for human clinical trials. Currently, pre-clinical work on these candidate vaccines is in progress so that eventually the human clinical trials can be initiated. In a multicentric project with Dr. Tyagi's laboratory being the nodal component, the genome of Mycobacterium indicus pranii known for its immunomodulatory properties against several diseases was completely sequenced at the University of Delhi South Campus to understand its unique properties. This was the first new bacterial genome to be completely sequenced and annotated in India.</p>
