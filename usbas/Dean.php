Dr. Shruti Aggarwal is a Professor in the University School of Basic and Applied Sciences, Guru Gobind Singh Indraprastha University, Delhi. She has obtained her M.Sc. Physics from Delhi University with specialization in Electronics and her Doctorate in the field of Solar Energy from Indian Institute of Technology (IIT) Delhi. As a Visiting Scientist to internationally known research lab, National Renewable Energy Laboratory (NREL), Golden, Colorado, USA from September 2013- September 2014, she worked on Silver nanowires based composites for their applications as transparent contacts as a replacement to conventionally used Indium tin oxide (ITO). She was a Visiting Professsor at Institute of physics, Academia Sinica, Taipei, Taiwan where she worked on advanced techniques of crystal growth and characterizations. She has been a project investigator to 7 research projects and two fellowships from funding agencies like AICTE, UGC, DST, IUAC, IoP, Academica Sinica, Taiwan. She is a recipient of AICTE Young Scientist Career Award. She had Visiting Associateship of Solar Energy Centre (now NISE), Minisitry of New and Renewable Energy (MNRE) in 2006-2007. She has been a member of governing councils of several orgraniations of repute like Indian Institute of Technology, Delhi (IIT Delhi), Solar Energy Society of India (SESI) which is an Indian counter-part of International Solar Energy Society. She has organized International and National conference and seminars in GGSIP University and for SESI, IIT Delhi Alumni and ASSOCHAM. She has attended various conferences/workshops/seminars as an invited speaker/paper presentation in India and in abroad. She has published more than 50 papers in International/National Journals & Conferences. She is currently guiding 1 PDF, 3 Research Scholars, and 1 JRF.

