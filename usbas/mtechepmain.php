  <h1 align="center" ><strong>Programmes: M.Tech. (Engineering Physics)</strong></h1>

	<br><center><b><a href="mtechep/mtechep280217.pdf">&nbsp;Scheme / Syllabus</a></b></center><br>
	
	

<p>M.Tech. (EP) (Approved by AICTE) is the first ever course at post-graduate level in India. The 
programme is planned in such a manner so as to bridge the gap between 
Engineering and Science streams. It meets the demands of the technical manpower 
required in several new emerging areas. This course prepare students to work in 
the Industries/Institues in the field of : Solar photovoltaics, Nuclear Energy 
Technologies, Nano Science and Technology, Embedded Systems, Computational 
simulations, visualization technology, networking. The Engineering Physics 
course at undergraduate level has already been started at the institutes like 
IIT's, DCE (changed to DTU) etc.</p>
<p>&nbsp;It is interdisciplinary and has courses from various fields of applied 
sciences and computer sciences. In Applied Sciences some of the focus areas are 
Photovoltaics, Embedded systems, Computational Physics, Nanoscience and 
Technology, Ion Beam Technology, Photonics.</p>
<p>&nbsp;In computational sciences courses on programming (C++, Java, Oracle 
etc.), networking, data base management systems, computer architecture, 
multimedia and visualization technology etc. are covered.<b><br></b></p>

