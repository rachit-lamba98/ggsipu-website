<p class="MsoNormal">
<span style="font-size:10.0pt;line-height:150%;
font-family:&quot;Arial&quot;,&quot;sans-serif&quot;">M. Tech in Nanoscience and 
Technology is a specialized, novel post graduate course initiated in 2008 under 
the Nano Mission Program of Ministry of Science and Technology, Government of 
India. GGSIP University is as one of the few Universities across India to be 
granted financial assistance by Department of Science &amp; Technology (DST) to 
start M. Tech in Nanoscience &amp; Technology. This program is geared towards 
generation of trained manpower in the field of Nanoscience and Technology. The 
course curriculum for this program was rigorously designed after thorough 
discussion with the experts. The course incorporates hands on training in lab 
courses and project work. As an integral part of the curriculum students undergo 
minor and major projects in Industries/ Universities/ Research organizations in 
India and abroad. Our program provides necessary skills to students and prepares 
them for the professional job market and exciting basic and interdisciplinary 
research careers in science. &nbsp;Many of our students are currently placed in 
attractive positions in industry and top research institutes in the country as 
well as abroad. </span>&nbsp;</p>
