<p>

 &nbsp;<p id="about" style="color: black !important"><b style="font-size: 17px; text-indent: 1px"><br>About M.Tech. (Engineering Physics)</b></p>
<p style="color: black !important">M.Tech. (EP) is the first ever course at post-graduate level in India. The
programme is planned in such a manner so as to bridge the gap between
Engineering and Science streams. It meets the demands of the technical manpower
required in several new emerging areas. This course prepare students to work in
the Industries/Institues in the field of : Solar photovoltaics, Nuclear Energy
Technologies, Nano Science and Technology, Embedded Systems, Computational
simulations, visualization technology, networking. The Engineering Physics
course at undergraduate level has already been started at the institutes like
IIT's, DCE (changed to DTU) etc.</p>
<p style="color: black !important">&nbsp;It is interdisciplinary and has courses from various fields of applied
sciences and computer sciences. In Applied Sciences some of the focus areas are
Photovoltaics, Embedded systems, Computational Physics, Nanoscience and
Technology, Ion Beam Technology, Photonics.</p>
<p style="color: black !important">&nbsp;In computational sciences courses on programming (C++, Java, Oracle
etc.), networking, data base management systems, computer architecture,
multimedia and visualization technology etc. are covered.<b><br></b></p><br>
<p style="color: black !important"><b style="font-size: 17px; text-indent: 1px">Highlights of the programme:</b></p>
<ul style="color: black !important">
	<li>&nbsp;The programme is approved by AICTE.</li>
	<li>&nbsp;Regular interactive sessions with eminent physicists and physicist
	turned entrepreneur/manager/administrator as part of the curriculum.</li>
	<li>&nbsp;Presentations cum Comprehensive Viva-Voce in each semester</li>
	<li>&nbsp;Project work by actual participation in Industry/ national
	research laboratories like SSPL, LASTEK, NPL, IUAC, VECC, CDAC etc.</li>
	<li>&nbsp;Working in the ongoing research projects at USS</li>
	<li>&nbsp;Visit to Industry/Research labs</li>
	<li>&nbsp;Students attend the national and international level conferences
	organized by FICCI, ASSOCHAM, SESI, EI group and various
	universities/institutes/ organizations.</li>
</ul><br>
